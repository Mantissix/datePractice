class calculator:
    def add(self, x, y):
        print(f'{x} + {y} = {x+y}')
        return x + y

    def sub(self, x, y):
        print(f'{x} - {y} = {x-y}')
        return x - y

    def multiply(self, x, y):
        print(f'{x} * {y} = {x*y}')
        return x * y

    def division(self, x, y):
        print(f'{x} / {y} = {x/y}')
        return x / y